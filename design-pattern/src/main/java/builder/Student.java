package builder;

public class Student {

    private String first_name;
    private String last_name;
    private int age;
    private int id;

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public int getAge() {
        return age;
    }

    public int getId() {
        return id;
    }

}
